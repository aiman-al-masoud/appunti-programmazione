# STRINGA
#  
# Una stringa è una sequenza immutabile di ... 
# stringhe di lunghezza=1. (Non esiste il tipo char).
#

s = "ciao mondo!"

# INDEXING & SLICING 

# proprio come nelle liste
print(s[0])
print(s[3:7])
print(s[1:7:2])

# APICI DOPPI == APICI SINGOLI

s = "ciao mondo!"
# oppure:
s = 'ciao mondo!'

# ESCAPE CHARS

'ho detto \'ciao mondo!\''
# oppure:
'ho detto "ciao mondo!"'


